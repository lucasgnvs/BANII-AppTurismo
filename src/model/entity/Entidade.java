package model.entity;

/**
 *
 * @author User
 */
public abstract class Entidade {
    private int cod;
    private String nome;
    
    public Entidade(int cod, String nome){
        this.cod = cod;
        this.nome = nome;
    }

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
}
